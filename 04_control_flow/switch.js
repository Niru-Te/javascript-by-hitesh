// switch statement

const month = 2

switch (month) {
    case 1:
        console.log("jan");
        
        break;
    case 2:
        console.log("fab");

        break;
    case 3:
        console.log("march");

        break;
    case 4:
        console.log("april");

        break;

    default:
        console.log("None of these");
        
        break;
}
